from elasticsearch8 import Elasticsearch
from elasticsearch8.exceptions import AuthenticationException
from setting import (ES_HOST, ES_PORT, ES_AUTH_NAME, ES_AUTH_PASSWORD, 
                    ES_FULLTEXT_SIZE, ES_VECTOR_SIZE, ES_INDEX, ES_FULLTEXT_INDEX_FILED, ES_VECTOR_FILED,
                    ES_RETURN_SIZE, ES_ALPHA)


class ESHelper:

    def __init__(self):
        """
        初始化Elasticsearch客户端。
        :param host: Elasticsearch服务器的地址。
        :param port: Elasticsearch服务器的端口。
        """
        try:
            self.es = Elasticsearch(
                hosts=[{"host": ES_HOST, "port": ES_PORT, "scheme": "http"}],
                basic_auth=(ES_AUTH_NAME, ES_AUTH_PASSWORD),
            )
        except AuthenticationException as e:
            print(f"Authentication failed: {e}")
            return None

    def search_by_fulltext(self, query):
        # 进行全文索引搜索
        """
        进行全文索引搜索并返回归一化分数。
        :param index: 索引名称。
        :param query: 搜索查询字符串。
        :param size: 返回结果的数量，默认为10。
        """
        try:
            # 构建全文查询结构
            full_index_query = {
                "query": {
                    "match": {
                        ES_FULLTEXT_INDEX_FILED: query
                    }
                },
                "size": ES_FULLTEXT_SIZE
            }
            response = self.es.search(index=ES_INDEX, body=full_index_query)
            
            # 提取结果和分数， 找出最大分数用于归一化
            # 这是 一级 hits
            one_hits = response["hits"]
            two_hits = one_hits["hits"]
            normalized_results = []
            if not two_hits:
                return normalized_results
            
            max_score = one_hits["max_score"] or 1.0
            
            # 返回归一化后的结果
            # raw_source 是真实的分数
            # normalized_score 是归一化之后的分数
            normalized_results = [
                {
                    "id": hit["_id"],
                    "source": hit["_source"],
                    "raw_score": hit["_score"],
                    "normalized_score": hit["_score"] / max_score
                }
                for hit in two_hits
            ]
            
            return normalized_results
        
        except Exception as e:
            print(f"full_index_search error: {e}")
            return []

    def search_by_calculate_similarity(self, query_vector, filter=None):
        """
        纯余弦相似度算法，计算两个向量之间的相似度。
        :param index_name: 索引的名称
        :param vector_field_name: 向量字段
        :param query_vector: 目标向量
        :param k: 返回的最近邻居的数量
        :param filter: 查询条件
        :return:
        """
        search_query = None
        # 增加 doc[params.field].size() == 0 的判断，防止文档中不存在向量字段导致报错
        source = f"doc[params.field].size() == 0 ? 0 : Math.max(cosineSimilarity(params.query_vector, params.field) + 1.0, 0.0)"
        if filter is not None and len(filter) > 0:
            search_query = {
                "size": ES_VECTOR_SIZE,
                "query": {
                    "script_score": {
                        "query": filter,
                        "script": {
                            "source": source,
                            "params": {"query_vector": query_vector, "field": ES_VECTOR_FILED},
                        },
                    }
                },
                "min_score": 1.5 # 由于相似度+1，min_score也需要相应调整
            }
        else:
            search_query = {
                "size": ES_VECTOR_SIZE,
                "query": {
                    "script_score": {
                        "query": {"match_all": {}},
                        "script": {
                            "source": source,
                            "params": {"query_vector": query_vector, "field": ES_VECTOR_FILED}
                        }
                    }
                },
                "min_score": 1.5 # 由于相似度+1，min_score也需要相应调整
            }
        response = self.es.search(index=ES_INDEX, body=search_query)
        normalized_results = []
        one_hits = response["hits"]
        two_hits = one_hits["hits"]
        # 如果没有数据直接返回空数组
        if not two_hits:
            return normalized_results
        
        # 下面是数据整理
        normalized_results = [
            {
                "id": hit["_id"],
                "source": hit["_source"],
                "raw_score": hit["_score"],
                "normalized_score": hit["_score"] - 1.0 # 归一化时减去之前加的1
            }
            for hit in two_hits
        ]
        
        return normalized_results
    
    def hybrid_search(self, text_query, vector):
        """
        混合搜索：结合全文和向量搜索结果，并按加权归一化分数排序。
        
        :param alpha: 向量搜索权重(0-1)，全文搜索权重为(1-alpha)
        """
        # 执行两种搜索
        fulltext_results = self.search_by_fulltext(text_query)
        vector_results = self.search_by_calculate_similarity(vector)
        # 创建 ID 到结果的映射
        combined_results = {}
        
        # 处理全文搜索结果
        for item in fulltext_results:
            doc_id = item["id"]
            combined_results[doc_id] = {
                "id": doc_id,
                "source": item["source"],
                "fulltext_score": item["normalized_score"] * (1 - ES_ALPHA),
                "vector_score": 0
            }
        
        # 处理向量搜索结果
        for item in vector_results:
            doc_id = item["id"]
            if doc_id in combined_results:
                combined_results[doc_id]["vector_score"] = item["normalized_score"] * ES_ALPHA
            else:
                combined_results[doc_id] = {
                    "id": doc_id,
                    "source": item["source"],
                    "fulltext_score": 0,
                    "vector_score": item["normalized_score"] * ES_ALPHA
                }
        
        # 计算最终分数并排序
        result_list = list(combined_results.values())
        for item in result_list:
            item["final_score"] = item["fulltext_score"] + item["vector_score"]
        
        # 按 final_score 降序排序并限制数量
        sorted_results = sorted(result_list, key=lambda x: x["final_score"], reverse=True)[:ES_RETURN_SIZE]
        return sorted_results


